<script lang="ts">
  import { fade, fly } from 'svelte/transition';
  import { goto } from '$app/navigation';
  import { createEventDispatcher, onMount } from 'svelte';
  import { toast } from '$lib/utils/toast';

  import { runProbeJob, type ProbeRunRequest } from '$lib/api/probes';
  import { pollProbeJob } from '$lib/features/jobs/probe-polling';
  import RunProbeButton from '$lib/components/common/RunProbeButton.svelte';
  import CredentialsFields from '$lib/components/common/CredentialsFields.svelte';
  import { resolveCredentials } from '$lib/auth/credentials-service';
  import { isValidHostname, isValidIPv4 } from '$lib/utils/host-validate';
  import { buildStorageEndpointConnectProbeRequest } from '$lib/probe/probe-builders';
  import { syncStorageRootDiscoveryForEndpoint } from '$lib/probe/probe-runner';
  import { extractDomainFromUsername } from '$lib/probe/storage-endpoint-probe';
  import { jobsStore } from '$lib/stores/app/jobs.store';

  import {
    createStorageEndpoint,
    listStorageEndpoints,
    type StorageEndpointOverview
  } from '$lib/api/storage-endpoints';
  import { createStorageRoot } from '$lib/api/storage-roots';
  import { listZones, type ZoneOverview } from '$lib/api/zones';
  import { listIdentitySources, type IdentitySourceListItem } from '$lib/api/identity-sources';

  /* =========================================================
     CONFIGURATION
  ========================================================= */
  export let variant: 'page' | 'modal' = 'modal';
  export let onDone: (() => void) | null = null;
  let isPage = variant === 'page';
  $: isPage = variant === 'page';

  // Optional prefill from parent pages (ex: /admin/zones)
  export let initialZoneId: number | null = null;
  export let lockZone = false;

  const requiresIdentitySource = false;
  const managedRootBadgeLabel = 'Integrated';
  const availableRootBadgeLabel = 'Available';

  const dispatch = createEventDispatcher<{
    done: { id: number; created_roots: number; failed_roots: number };
  }>();

  /* =========================================================
     STATE
  ========================================================= */
  let step = 1;
  let direction: 'forward' | 'back' = 'forward';

  let model: {
    protocol: 'smb';

    name?: string;
    description?: string;

    host?: string;
    port?: number;

    username?: string;

    /**
     * 🔐 UI ONLY — never sent directly
     */
    password?: string;

    /**
     * Generated by backend secret management.
     */
    secret_ref?: string | null;

    roots?: string[];
    zone_id?: number | null;
    identity_source_id?: number | null;
  } = {
    protocol: 'smb',
    roots: [],
    secret_ref: null,
    zone_id: null,
    identity_source_id: null
  };

  let zones: ZoneOverview[] = [];
  let zonesLoading = false;
  let zonesError: string | null = null;

  type IdentitySourceOption = IdentitySourceListItem;

  let identitySources: IdentitySourceOption[] = [];
  let identitySourcesLoading = false;
  let identitySourcesError: string | null = null;
  let existingEndpoints: StorageEndpointOverview[] = [];

  const wizardProbeJobInput = () => ({
    entityType: 'storage-endpoint-wizard',
    entityId: `${String(model.host ?? 'new')}::${String(model.name ?? 'endpoint')}`,
    action: 'probe'
  } as const);

  const wizardDiscoveryJobInput = () => ({
    entityType: 'storage-endpoint-wizard',
    entityId: `${String(model.host ?? 'new')}::${String(model.name ?? 'endpoint')}`,
    action: 'discovery'
  } as const);

  onMount(async () => {
    zonesLoading = true;
    identitySourcesLoading = true;
    zonesError = null;
    identitySourcesError = null;
    try {
      zones = await listZones(fetch);
      existingEndpoints = await listStorageEndpoints(fetch);
      const rows = await listIdentitySources(fetch);
      identitySources = rows as IdentitySourceListItem[];

      // Prefill zone if provided
      if (initialZoneId !== null && initialZoneId !== undefined) {
        model.zone_id = initialZoneId;
      }
    } catch (e) {
      const message = e?.message ?? 'Unable to load wizard data.';
      zonesError = message;
      identitySourcesError = message;
    } finally {
      zonesLoading = false;
      identitySourcesLoading = false;
    }
  });

  $: activeAdIdentitySources = identitySources.filter((s) => {
    const kind = String(s?.type ?? '').trim().toUpperCase();
    const active = s?.is_active !== false;
    return active && (kind === 'AD' || kind === 'LDAP' || kind === 'LDAPS');
  });

  /* =========================================================
     VALIDATION
  ========================================================= */

  let showPassword = false;
  let resolveHint: { ok: boolean; message: string } | null = null;
  let touched: Record<string, boolean> = {};
  let showTestHint = false;
  let showContinueHint = false;

  const touch = (field: 'name' | 'host' | 'port' | 'username' | 'password' | 'identity_source' | 'zone') => {
    touched = { ...touched, [field]: true };
  };

  const touchAllStep2 = () => {
    touched = {
      ...touched,
      name: true,
      host: true,
      port: true,
      username: true,
      password: true,
      identity_source: true,
      zone: true
    };

    showTestHint = !canTestConnection;
    showContinueHint = !canContinueStep2;
  };

  function resolveHostInput() {
    const value = (model.host ?? '').trim();
    if (!value) {
      resolveHint = { ok: false, message: 'Type un hostname ou une IP.' };
      return;
    }
    if (isValidIPv4(value)) {
      resolveHint = { ok: true, message: 'IP valide.' };
      return;
    }
    if (isValidHostname(value)) {
      resolveHint = { ok: true, message: 'Hostname valide.' };
      return;
    }
    resolveHint = { ok: false, message: 'Format hostname/IP invalide.' };
  }

  function resolveTestTarget(): string | null {
    const v = (model.host ?? '').trim();
    if (!v) return null;
    if (isValidIPv4(v) || isValidHostname(v)) return v;
    return null;
  }

  function normalizedHost(value?: string | null): string {
    return String(value ?? '').trim().toLowerCase();
  }

  $: currentNormalizedHost = normalizedHost(model.host);
  $: duplicateEndpoint = existingEndpoints.find((ep) => {
    const candidateHost = normalizedHost(String(ep?.host ?? ''));
    return Boolean(candidateHost && candidateHost === currentNormalizedHost);
  });

  /* =========================================================
     STEP 1 — TYPE
   ========================================================= */
  let selectedProtocol: 'smb' | null = null;

  function selectProtocol(p: 'smb') {
    selectedProtocol = p;
    model.protocol = p;
  }

  $: canContinueStep1 = selectedProtocol !== null;

  /* =========================================================
     NAVIGATION
  ========================================================= */
  function go(next: number) {
    direction = next > step ? 'forward' : 'back';
    step = next;
  }

  const panelIn = () => ({
    x: direction === 'forward' ? 40 : -40,
    duration: 220
  });

  function goStep2() {
    if (!canContinueStep1) return;
    go(2);
  }

  /* =========================================================
     DERIVED RULES
  ========================================================= */
  $: model.port ??= 445;

  const required = (v?: string | number | null) =>
    v !== undefined && v !== null && String(v).trim().length > 0;

  $: errName = Boolean(touched.name) && !required(model.name);
  $: errHost = Boolean(touched.host) && !required(model.host);
  $: errHostFormat =
    Boolean(touched.host) &&
    required(model.host) &&
    !isValidIPv4(model.host) &&
    !isValidHostname(model.host);
  $: errPort = Boolean(touched.port) && !required(model.port);
  $: errUsername = Boolean(touched.username) && !required(model.username);
  $: errPassword = Boolean(touched.password) && !required(model.password);
  $: errIdentitySource =
    requiresIdentitySource && Boolean(touched.identity_source) && !required(model.identity_source_id);
  $: errZone = Boolean(touched.zone) && !required(model.zone_id);

  $: if (showTestHint && canTestConnection) showTestHint = false;
  $: if (showContinueHint && canContinueStep2) showContinueHint = false;

  $: canContinueStep2 =
    required(model.name) &&
    required(model.zone_id) &&
    (!requiresIdentitySource || required(model.identity_source_id)) &&
    required(model.host) &&
    (isValidIPv4(model.host) || isValidHostname(model.host)) &&
    !duplicateEndpoint;

  function onIdentitySourceChange(event: Event) {
    const raw = (event.currentTarget as HTMLSelectElement).value;
    model.identity_source_id = raw ? Number(raw) : null;
    touch('identity_source');
  }

  $: canTestConnection =
    canContinueStep2 &&
    required(model.port) &&
    required(model.username) &&
    required(model.password);

  /* =========================================================
     STEP 2 — PROBE (V1.1)
  ========================================================= */
  type TestStatus = 'idle' | 'running' | 'success' | 'failed';
  let testStatus: TestStatus = 'idle';
  let testError: string | null = null;
  let connectionTested = false;
  let lastSuccessfulProbeAt: string | null = null;
  let lastSuccessfulProbeMessage: string | null = null;

  async function ensureSecretRef(
    mode: 'create' | 'edit' = 'create',
    options?: { clearPassword?: boolean }
  ): Promise<string | null> {
    const clearPassword = options?.clearPassword ?? true;
    const name = `storage-endpoint/${model.protocol}/${(model.host || model.name || 'endpoint').trim()}`;
    const resolved = await resolveCredentials(fetch, {
      username: model.username ?? null,
      password: model.password ?? null,
      secret_ref: model.secret_ref ?? null
    }, { secretName: name, mode });

    model.secret_ref = resolved.secret_ref ?? null;
    if (clearPassword) {
      // never keep plaintext password after resolving
      model.password = '';
    }
    return model.secret_ref;
  }

  function buildConnectProbeRequest(secretRef: string | null = model.secret_ref ?? null): ProbeRunRequest | null {
    const target = resolveTestTarget();
    if (!target) {
      return null;
    }
    return buildStorageEndpointConnectProbeRequest({
      protocol: model.protocol,
      host: target,
      port: model.port ?? 445,
      username: model.username ?? null,
      domain: extractDomainFromUsername(model.username),
      secretRef,
      password: secretRef ? null : model.password ?? null,
      discover: false,
      timeoutSec: 20,
      uiOrigin: 'wizard'
    });
  }

  $: if (step === 3 && discoverStatus === 'idle') {
    startDiscovery();
  }

  /* =========================================================
     STEP 3 — DISCOVERY
   ========================================================= */
  let governedRoots: string[] = [];
  let discoverStatus: 'idle' | 'running' | 'success' | 'failed' = 'idle';
  let discoverError: string | null = null;
  let rootsQuery = '';
  let detectedRoots: string[] = [];
  let alreadyGovernedRoots: string[] = [];

  const blockedRoots = ['SYSVOL', 'NETLOGON'];

  const normalizeRoot = (value: string) => value.trim();

  const rootLabel = (value: string) => {
    const normalized = normalizeRoot(value);
    const parts = normalized.split(/[\\/]+/).filter(Boolean);
    return parts[parts.length - 1] || normalized;
  };

  const isBlockedRoot = (value: string) =>
    blockedRoots.includes(rootLabel(value).toUpperCase());

  const isAlreadyGoverned = (value: string) => {
    const normalized = normalizeRoot(value);
    return alreadyGovernedRoots.some((root) => normalizeRoot(root) === normalized);
  };

  const isSelected = (value: string) => governedRoots.includes(normalizeRoot(value));

  const govern = (value: string) => {
    if (isAlreadyGoverned(value) || isBlockedRoot(value)) return;
    const normalized = normalizeRoot(value);
    if (governedRoots.includes(normalized)) return;
    governedRoots = [...governedRoots, normalized];
  };

  const ungovern = (value: string) => {
    const normalized = normalizeRoot(value);
    governedRoots = governedRoots.filter(root => root !== normalized);
  };

  const toggleGovern = (value: string) => {
    if (isAlreadyGoverned(value) || isBlockedRoot(value)) return;
    if (isSelected(value)) {
      ungovern(value);
      return;
    }
    govern(value);
  };

  const selectAllFiltered = () => {
    const selectable = filteredDetectedRoots
      .filter(root => !isAlreadyGoverned(root))
      .filter(root => !isBlockedRoot(root));
    const merged = new Set([...governedRoots, ...selectable.map(normalizeRoot)]);
    governedRoots = Array.from(merged);
  };

  const clearSelection = () => {
    governedRoots = [];
  };

  const invertSelection = () => {
    const next = new Set(governedRoots);
    for (const root of filteredDetectedRoots) {
      if (isAlreadyGoverned(root) || isBlockedRoot(root)) continue;
      const normalized = normalizeRoot(root);
      if (next.has(normalized)) next.delete(normalized);
      else next.add(normalized);
    }
    governedRoots = Array.from(next);
  };

  type RootStatusFilter = 'all' | 'available' | 'selected' | 'already_governed';
  let rootStatusFilter: RootStatusFilter = 'available';
  let rootPage = 1;
  let rootsPerPage = 8;

  $: filteredDetectedRoots = (() => {
    const query = rootsQuery.trim().toLowerCase();
    if (!query) return detectedRoots;
    return detectedRoots.filter(root => root.toLowerCase().includes(query));
  })();

  $: visibleDetectedRoots = filteredDetectedRoots.filter(root => !isBlockedRoot(root));
  $: selectableDetectedRoots = visibleDetectedRoots.filter((root) => !isAlreadyGoverned(root));
  $: selectedVisibleRoots = visibleDetectedRoots.filter((root) => isSelected(root));
  $: rootRows = visibleDetectedRoots.filter((root) => {
    if (rootStatusFilter === 'selected') return isSelected(root);
    if (rootStatusFilter === 'already_governed') return isAlreadyGoverned(root);
    if (rootStatusFilter === 'available') return !isAlreadyGoverned(root) && !isBlockedRoot(root);
    return true;
  });
  $: totalRootPages = Math.max(1, Math.ceil(rootRows.length / rootsPerPage));
  $: if (rootPage > totalRootPages) rootPage = totalRootPages;
  $: paginatedRootRows = rootRows.slice((rootPage - 1) * rootsPerPage, rootPage * rootsPerPage);
  $: rootRangeStart = rootRows.length === 0 ? 0 : (rootPage - 1) * rootsPerPage + 1;
  $: rootRangeEnd = Math.min(rootPage * rootsPerPage, rootRows.length);
  $: selectedPreviewRoots = governedRoots.slice(0, 4);
  $: hiddenSelectedCount = Math.max(0, governedRoots.length - selectedPreviewRoots.length);
  $: endpointHostLabel = String(model.host ?? '').trim() || 'Storage endpoint';
  $: lastScanLabel =
    discoverStatus === 'running'
      ? 'running'
      : discoverStatus === 'success'
        ? 'just now'
        : discoverStatus === 'failed'
          ? 'failed'
          : 'not scanned';

  async function startDiscovery() {
    if (!canContinueStep2) return;

    discoverStatus = 'running';
    discoverError = null;
    detectedRoots = [];
    alreadyGovernedRoots = [];
    jobsStore.startJob(wizardDiscoveryJobInput(), {
      message: 'Discovery running'
    });

    try {
      const secretRef = await ensureSecretRef('create');

      const target = resolveTestTarget();
      if (!target) {
        discoverStatus = 'failed';
        discoverError = 'Hostname / IP invalide';
        jobsStore.failJob(wizardDiscoveryJobInput(), {
          message: discoverError,
          error: discoverError
        });
        return;
      }

      const { job_id } = await runProbeJob(fetch, {
        kind: 'storage-endpoint',
        protocol: model.protocol,
        scope: 'read',
        target: {
          host: target,
          port: model.port
        },
        auth: {
          mode: 'ntlm',
          username: model.username,
          domain: extractDomainFromUsername(model.username),
          secret_ref: secretRef ?? undefined,
          password: secretRef ? undefined : model.password
        },
        options: {
          timeout_sec: 30,
          discover: true
        },
        context: { ui_origin: 'wizard' }
      });

      const polled = await pollProbeJob(fetch, job_id, {
        intervalMs: 3000,
        intervalMsByAttempt: (attempt) => (attempt < 2 ? 3000 : attempt < 6 ? 5000 : 8000)
      });

      if (polled.status === 'success') {
        const job = polled.payload as { result?: { details?: { roots?: string[] }; roots?: string[] } };
        const roots =
          (job?.result?.details?.roots as string[]) ||
          (job?.result?.roots as string[]) ||
          [];
        detectedRoots = Array.isArray(roots) ? roots : [];
        discoverStatus = 'success';
        lastSuccessfulProbeAt = new Date().toISOString();
        lastSuccessfulProbeMessage = `discovery_ok:${detectedRoots.length}`;
        jobsStore.succeedJob(wizardDiscoveryJobInput(), {
          message: 'Discovery success',
          summary: {
            rootsCount: detectedRoots.length
          },
          payload: job
        });
      } else {
        discoverStatus = 'failed';
        discoverError = polled.errorMessage ?? 'Discovery failed';
        jobsStore.failJob(wizardDiscoveryJobInput(), {
          message: discoverError,
          error: discoverError,
          payload: polled.payload
        });
      }
    } catch (e) {
      discoverStatus = 'failed';
      discoverError = e?.message ?? 'Unable to run discovery';
      jobsStore.failJob(wizardDiscoveryJobInput(), {
        message: discoverError,
        error: discoverError
      });
    }
  }

  /* =========================================================
     FINISH
  ========================================================= */
  async function finish() {
    if (!model.name) return;
    if (!required(model.zone_id)) {
      touch('zone');
      toast.error('A zone is required to create a storage endpoint.');
      return;
    }

    try {
      const secretRef = await ensureSecretRef('create');

      const created = await createStorageEndpoint(fetch, {
        name: model.name,
        description: model.description ?? null,
        endpoint_type: model.protocol,
        protocol: model.protocol,
        identity_source_id: model.identity_source_id ?? null,
        host: model.host ?? null,
        port: model.port ?? null,
        auth_type: 'ntlm',
        bind_dn: model.username ?? null,
        bind_password_ref: secretRef,
        zone_id: Number(model.zone_id),
        is_active: true,
        status: 'active',
        last_probe_status: lastSuccessfulProbeAt ? 'success' : null,
        last_probe_at: lastSuccessfulProbeAt,
        last_probe_message: lastSuccessfulProbeMessage
      });

      let createdRoots = 0;
      let failedRoots = 0;

      if (governedRoots.length > 0) {
        const res = await Promise.allSettled(
          governedRoots.map(locator =>
            createStorageRoot(fetch, {
              storage_endpoint_id: created.id,
              name: locator.split('\\').pop() || locator,
              root_path: locator,
              status: 'active'
            })
          )
        );

        for (const r of res) r.status === 'fulfilled' ? createdRoots++ : failedRoots++;
      }

      if (discoverStatus === 'success') {
        try {
          await syncStorageRootDiscoveryForEndpoint(fetch, created.id, {
            discovered_at: lastSuccessfulProbeAt ?? new Date().toISOString(),
            roots: detectedRoots
          });
        } catch (syncError) {
          console.warn('storage_endpoint_wizard.discovery_sync_failed', syncError);
          toast.warning('Storage roots were created, but discovery sync must be refreshed.');
        }
      }

      toast.success('Storage endpoint created.');
      dispatch('done', { id: created.id, created_roots: createdRoots, failed_roots: failedRoots });

      if (onDone) onDone();
      else if (isPage) goto('/admin/storage-endpoints');
    } catch (e) {
      toast.error(e?.message ?? 'Creation failed');
    }
  }
</script>

