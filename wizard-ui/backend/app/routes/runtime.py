@router.post("/test-port")
async def test_port(payload: PortTestPayload):
    """
    Test simple de connectivité réseau (TCP/UDP).
    """
    start = time.time()

    try:
        if payload.protocol == "tcp":
            with socket.create_connection(
                (payload.host, payload.port),
                timeout=1.5,
            ):
                pass
        else:
            sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
            sock.sendto(b"ping", (payload.host, payload.port))
            sock.close()

        latency = round((time.time() - start) * 1000, 2)

        return {
            "ok": True,
            "latency_ms": latency,
        }

    except Exception as exc:
        return {
            "ok": False,
            "error": str(exc),
        }


