@router.post("/generate-ansible")
async def generate_ansible_pack(config: RuntimeConfig):
    """
    Génère le bundle runtime BornToShare :
      - b2s-runtime-manifest.json
      - inventory.ini
      - group_vars/all.yml
      - deploy_borntoshare.yml
    """
    out_dir = ensure_output_dir()

    # --------------------------------------------------------
    # 1️⃣ Manifest JSON
    # --------------------------------------------------------
    manifest_path = out_dir / "b2s-runtime-manifest.json"
    manifest_path.write_text(
        json.dumps(config.dict(), indent=2, ensure_ascii=False),
        encoding="utf-8",
    )

    # --------------------------------------------------------
    # 2️⃣ Inventory minimal
    # --------------------------------------------------------
    inventory_path = out_dir / "inventory.ini"
    inventory_path.write_text(
        "[borntoshare]\nlocalhost ansible_connection=local\n",
        encoding="utf-8",
    )

    # --------------------------------------------------------
    # 3️⃣ group_vars/all.yml
    # --------------------------------------------------------
    group_vars_dir = out_dir / "group_vars"
    group_vars_dir.mkdir(exist_ok=True)

    db_root = config.db_root or {}
    db_app = config.db_app or {}
    ports = config.ports or {}

    group_all_content = f"""---
# ============================================================
# BornToShare – Runtime variables (generated by Wizard)
# ============================================================

# --------------------------
# DATABASE
# --------------------------
b2s_db_host: {db_root.get("host", "localhost")}
b2s_db_port: {db_root.get("port", 3306)}
b2s_db_name: {config.db_name}
b2s_db_user: {db_app.get("user", "b2s_app")}
b2s_db_password: "{db_app.get("password", "")}"

# --------------------------
# PORTS
# --------------------------
b2s_port_auth: {ports.get("auth", 8001)}
b2s_port_gov: {ports.get("governance", 8002)}
b2s_port_frontend: {ports.get("frontend", 8081)}
"""

    group_all_path = group_vars_dir / "all.yml"
    group_all_path.write_text(group_all_content, encoding="utf-8")

    # --------------------------------------------------------
    # 4️⃣ Playbook minimal
    # --------------------------------------------------------
    playbook_path = out_dir / "deploy_borntoshare.yml"

    playbook_content = """---
- name: Déployer BornToShare (Podman)
  hosts: borntoshare
  become: false
  vars_files:
    - group_vars/all.yml

  tasks:
    - name: Charger le manifest runtime
      ansible.builtin.set_fact:
        b2s_runtime: "{{ lookup('file', 'b2s-runtime-manifest.json') | from_json }}"

    - name: Créer réseau BornToShare
      containers.podman.podman_network:
        name: b2s_net
        state: present
"""

    playbook_path.write_text(playbook_content, encoding="utf-8")

    return {
        "ok": True,
        "message": "Pack Ansible généré",
        "files": [
            str(manifest_path),
            str(inventory_path),
            str(group_all_path),
            str(playbook_path),
        ],
    }


